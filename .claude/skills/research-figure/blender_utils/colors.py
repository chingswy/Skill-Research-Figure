# ---------------------------------------------------------------------------
# Color resolution function
# ---------------------------------------------------------------------------

_hex2rgb = lambda x: list(map(lambda v: int('0x' + v, 16) / 255., [x[:2], x[2:4], x[4:]]))


def get_rgb(pid):
    """Resolve a color identifier to an RGBA list.

    Args:
        pid: One of:
            - int → index into colors_rgb
            - str in colors_table → named color
            - 6-char hex string → parse as RGB hex
            - list/tuple → use directly

    Returns:
        [r, g, b, 1.0] list
    """
    if isinstance(pid, str) and pid in colors_table:
        col = colors_table[pid]
    elif isinstance(pid, str):
        col = _hex2rgb(pid)
    elif isinstance(pid, (list, tuple)):
        col = list(pid[:3])
    else:
        col = list(colors_rgb[pid % len(colors_rgb)])
    return list(col) + [1.0]
